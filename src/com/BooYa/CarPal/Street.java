public class Street {
    private int house_num;
    private int street_code;
    private String street_name;
    private String street_name_eng;

    public int getHouse_num() {
        return house_num;
    }

    public void setHouse_num(int house_num) {
        this.house_num = house_num;
    }

    public int getStreet_code() {
        return street_code;
    }

    public void setStreet_code(int street_code) {
        this.street_code = street_code;
    }

    public String getStreet_name() {
        return street_name;
    }

    public void setStreet_name(String street_name) {
        this.street_name = street_name;
    }

    public String getStreet_name_eng() {
        return street_name_eng;
    }

    public void setStreet_name_eng(String street_name_eng) {
        this.street_name_eng = street_name_eng;
    }
}
